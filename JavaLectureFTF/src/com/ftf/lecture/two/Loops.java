public class Loops {

	public static void main(String[] args) {

		//==================================================================
		// For Loop
		//==================================================================
		
		for (int i=0; i<11; i++) {
			System.out.println("Hello World!");
		}
		
		System.out.println("\n\r");
		
		//==================================================================
		// While Loop
		//==================================================================
		
		int j = 0;
		
		while (j <=10) {
			System.out.println("Hello Java!");
			j++;
		}

	}

}
